void init()
{
        int flag;
        flag = (int) query("room_flag");
        if(flag & 1) set("valid_startroom",1);
        if(flag & 2) set("NONPC",1);
        if(flag & 4) set("no_fight",1);
        if(flag & 8) set("no_magic",1);
        if(flag & 16) set("indoors","residence");
        add_action("do_invite","invite");
        add_action("do_setowner","setowner");
        add_action("do_list","list");
        add_action("do_knock","knock");
        add_action("do_push","push");
        add_action("do_pull","pull");
}

int do_knock(string arg)
{
	if(arg=="ring")
	{
		message_vision(HIC"$N用门环在门上敲了两下，门里传出人声：“主~~~人不在家，到~~~哪儿去了，就~~~~不告诉你！”\n"NOR, this_player());
		message_vision(HIC"$N不由得怔在那里，呆若木鸡。 \n"NOR, this_player());
	return 1;
	}
	write("你要敲什么？\n");
	return 1;
}

int do_push(string arg)
{	
	if(arg=="door" || arg=="门")
	{
		message_vision(HIC"$N伸手推了推门，门突然一下子就开了，然后猛地弹回来，", this_player());
		message_vision("$N猝不及防，", this_player());
		message_vision("被门打了个头昏眼花。\n"NOR, this_player());
	return 1;
	}
	write("你要推开什么？\n");
	return 1;	
}

int do_pull(string arg)
{
	if(arg=="door" || arg=="门")
	{
		message_vision(HIC"$N伸手拉了拉门，门“吱呀”一声开了。\n"NOR, this_player());
	        if( !query("exits/north") ) 
	        {
        		set("exits/north", __DIR__"chuchu2");
        		call_out("close_path", 5);
        	}
		return 1;
	}
	write("你要拉开什么？\n");
	return 1;
}

void close_path()
{
        if( !query("exits/north") ) return;
        message("vision","门又关上了。\n", this_object() );
        delete("exits/north");
}       

int valid_enter(object me)
{
        int flag;
// means no enter at the beginning.
        int enter = 0;
    flag = (int) query("room_flag");
// always let owner go in:
        if((string)me->query("id") == (string)query("owner"))
        enter = 1;
        if(flag & 16)
        enter = 1;
        if(flag & 512)
        if((string)me->query("id") == (string)query("owner"))
        enter = 1;
        if(flag & 1024)
        if((string)me->query("class") == (string)query("class"))
        enter = 1;
        if(flag & 2048) 
        {
                if(strlen(query("invite")) && strsrch((string)query("invite"),"["+(string)me->query("id")+"]") != -1)
           enter = 1;
        }
//      write(sprintf("%d", enter));    
        return enter;
}
int do_list()
{
        object me;
        string *list,invitelist;
        me = this_player();
        if((string)me->query("id") != (string)query("owner"))
        return 0;
        invitelist = query("invite");
        if (strlen(invitelist))
        {
                list = explode("]"+invitelist+"[", "][");
                write("你邀请了下列玩家到你的房间:\n");
                for(int i=0;i<sizeof(list);i++)
                        write(list[i]+"\n");
        }else
                write("你没有邀请任何人到你的房间。\n");
        return 1;
} 
int do_invite(string arg)
{
        object me;
        string invitelist;
        me= this_player();
        if((string)me->query("id") != (string)query("owner"))
        return 0;
        if( !arg )
        {
                write("你要邀请谁? \n");
                return 1;
        }
        invitelist = (string)query("invite");
        if(strlen(invitelist) && strsrch(invitelist,"["+arg+"]") != -1)
        {
                //if the person has already been invited, remove it from invite list.
                invitelist = replace_string(invitelist,"["+arg+"]","");
           set("invite",invitelist);
                write("你将"+arg+"从你的邀请名单中除去。\n");
        }else
        {
                //invite the person.
                if( find_player(arg))
                {
                        if(!strlen(invitelist))
                                set("invite","["+arg+"]");
                        else
                                set("invite",invitelist+"["+arg+"]");
                        write("你邀请"+arg+"来你的房间。\n");
                }else
                {
                        write("咦... 有这个人吗?\n");
                }
        }
        return 1;
} 
int do_setowner(string arg)
{
        string oldowner;
        oldowner = (string)query("owner");
        set("owner",arg);
        write("你将房间的主人暂时设为"+arg+"。\n");
        call_out("set_back",60, oldowner);
        return 1;
} 
int set_back(string oldowner)
{
        set("owner", oldowner);
        return 1;
}
    
